#include "operations.h"
/**
 * add - writes the character c to stdout
 * @a: The character to print
 * @b: The character to print
 * Return: On success 1.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - writes the character c to stdout
 * @a: The character to print
 * @b: The character to print
 * Return: On success 1.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - writes the character c to stdout
 * @a: The character to print
 * @b: The character to print
 * Return: On success 1.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - writes the character c to stdout
 * @a: The character to print
 * @b: The character to print
 * Return: On success 1.
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - writes the character c to stdout
 * @a: The character to print
 * @b: The character to print
 * Return: On success 1.
 */
int mod(int a, int b)
{
	return (a % b);
}
